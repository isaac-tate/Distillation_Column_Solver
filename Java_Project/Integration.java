public abstract class Integration{//a generalized class that can hold a number of different integration methods
  
  /*Class: Integration
   * 
   * Integration is a abstract parent class that holds a calculate method used for different integration methods
   * All child classes of Integration contain a calculate method that returns the integrated value of two arrays
   * 
   */
  
    public abstract double calculate(double [] x, double [] y);
      
}
                                   
     